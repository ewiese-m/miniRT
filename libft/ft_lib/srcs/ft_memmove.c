#include "ft_lib.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t			index;

	index = 0;
	if (dest == src)
		return (dest);
	if (dest < src)
	{
		while (index < n)
		{
			((unsigned char *)dest)[index] = ((unsigned char *)src)[index];
			index++;
		}
	}
	else
	{
		while (n > 0)
		{
			((unsigned char *)dest)[n - 1] = ((unsigned char *)src)[n - 1];
			n--;
		}
	}
	return (dest);
}
